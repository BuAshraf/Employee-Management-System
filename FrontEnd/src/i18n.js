const I18nContext = createContext();

export function I18nProvider({ children }) {
  const [lang, setLang] = useState('en');
  const [theme, setTheme] = useState('light');

  // Initialize theme from localStorage or system preference
  useEffect(() => {
    const savedTheme = localStorage.getItem('theme');
    const savedLang = localStorage.getItem('language');

    if (savedTheme) {
      setTheme(savedTheme);
    } else {
      // Check system preference
      const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
      setTheme(prefersDark ? 'dark' : 'light');
    }

    if (savedLang) {
      setLang(savedLang);
    }
  }, []);

  // Apply theme to document
  useEffect(() => {
    const root = document.documentElement;
    if (theme === 'dark') {
      root.classList.add('dark');
    } else {
      root.classList.remove('dark');
    }
    localStorage.setItem('theme', theme);
  }, [theme]);

  // Save language preference
  useEffect(() => {
    localStorage.setItem('language', lang);
  }, [lang]);

  // Enhanced translation function with nested key support
  const t = (key) => {
    // Handle nested keys like 'departmentNames.it'
    if (typeof key !== 'string') return '';
    if (key.includes('.')) {
      const keys = key.split('.');
      let value = translations[lang];
      for (const k of keys) {
        value = value?.[k];
        if (value === undefined) break;
      }
      // Prevent returning objects to React
      if (value && typeof value === 'object') return key;
      return value ?? key;
    }
    // Handle simple keys
    const value = translations[lang]?.[key];
    if (value && typeof value === 'object') return key;
    return value ?? key;
  };

  // Theme toggle with GSAP animation
  const toggleTheme = () => {
    const newTheme = theme === 'light' ? 'dark' : 'light';

    // GSAP animation for theme transition
    gsap.to("body", {
      duration: 0.3,
      ease: "power2.inOut",
      backgroundColor: newTheme === 'dark' ? '#1a1a1a' : '#ffffff',
      onComplete: () => {
        setTheme(newTheme);
      }
    });
  };

  const value = {
    lang,
    setLang,
    theme,
    setTheme,
    toggleTheme,
    t,
    gsap,
    useGSAP
  };

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
